package ru.mentee.power.exception;

public class SASTException extends Exception {
    public SASTException() {
        super();
    }

    public SASTException(String message) {
        super(message);
    }

    public SASTException(String message, Throwable cause) {
        super(message, cause);
    }

    public SASTException(Throwable cause) {
        super(cause);
    }
}
